package incluseMind;

public class Usuario {
	int idade;
	char genero;
	String avisos;
	
	public char getGenero() {
		return genero;
	}
	public void setGenero(char genero) {
		this.genero = genero;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public String getAvisos() {
		return avisos;
	}
	public void setAvisos(String avisos) {
		this.avisos = avisos;
	}
	
}