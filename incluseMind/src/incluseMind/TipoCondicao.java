package incluseMind;

public class TipoCondicao {
	boolean TEA;
	boolean TDAH;
	int nivel_TEA;
	int nivel_TDAH;
	
	public boolean isTEA() {
		return TEA;
	}
	public void setTEA(boolean tEA) {
		TEA = tEA;
	}
	public boolean isTDAH() {
		return TDAH;
	}
	public void setTDAH(boolean tDAH) {
		TDAH = tDAH;
	}
	public int getNivel_TEA() {
		return nivel_TEA;
	}
	public void setNivel_TEA(int nivel_TEA) {
		this.nivel_TEA = nivel_TEA;
	}
	public int getNivel_TDAH() {
		return nivel_TDAH;
	}
	public void setNivel_TDAH(int nivel_TDAH) {
		this.nivel_TDAH = nivel_TDAH;
	}
	
}
